package com.gridnine.testing.filter;

import java.util.List;

/**
 * Интерфейс для фильтрации списка объектов.
 * Каждый фильтр реализует метод фильтрации, применимый к конкретному типу данных.
 *
 * @param <T> тип объектов, которые фильтруются
 */
public interface Filter<T> {

    /**
     * Фильтрует список объектов на основе конкретных условий фильтра.
     *
     * @param items список объектов для фильтрации
     * @return отфильтрованный список объектов
     */
    List<T> filter(List<T> items);
}
